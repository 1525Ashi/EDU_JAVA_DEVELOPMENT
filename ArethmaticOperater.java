package Operaters;


//+,-,*,/,%
public class ArethmaticOperater {
	
	int a=10;
	int b=20;
	int c=a+b;
	
	int f=10;
	int e=20;
	int d=e-f;
	
	int h=20;
	int g=30;
	int i=g/h;
	
	int z=10;
	int x=20;
	int v=z*x;
	
	int o=10;
	int n=20;
	int m=n%o;


	public static void main(String[] args) {
		
		ArethmaticOperater ash = new ArethmaticOperater();
		
		System.out.println(ash.c);
		System.out.println(ash.d);
		System.out.println(ash.i);
		System.out.println(ash.v);
		System.out.println(ash.m);
		
		

	}

}
